/**
 * 获取流通股本
 * @param symbol 股票代码（如 600519 或 sh600519）
 * @returns 流通股本（股），找不到返回默认值
 */
export function getStockCapital(symbol: string): number {
  // 提取纯数字代码
  const code = symbol.replace(/[^0-9]/g, '');
  
  // 查找数据库
  const capital = STOCK_CAPITAL_DB[code];
  if (capital) {
    return capital;
  }
  
  // 根据代码范围返回默认估算值（仅作为fallback）
  const codeNum = parseInt(code);
  if (codeNum >= 600000) {
    return 5000000000; // 大盘默认50亿股
  } else if (codeNum >= 300000) {
    return 1000000000; // 创业板默认10亿股
  } else if (codeNum >= 2000) {
    return 2000000000; // 中小板默认20亿股
  }
  return 3000000000; // 主板默认30亿股
}
